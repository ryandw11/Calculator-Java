package functions;

public class Method {
		
	public static double add(double num1, double num2){
		double output;
		output = num1 + num2;
	return output;
	}
	
	public static double minus(double num1, double num2){
		double output;
		output = num1 - num2;
	return output;
	}
	
	public static double multi(double num1, double num2){
		double output;
		output = num1 * num2;
	return output;
	}
	
	public static double divide(double num1, double num2){
		double output;
		output = num1 / num2;
	return output;
	}

}
